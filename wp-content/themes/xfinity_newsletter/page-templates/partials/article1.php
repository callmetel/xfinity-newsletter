			<h4 class="letter-heading">Q:</h4>
			<h4>What opportunities can we explore with voice-enabled technology?</h4>	
		</div>
		<div class="answer">
			<h4 class="letter-heading">A:</h4>
			<h4>Our top 3 Opportunities:</h4>	
			<h3 class="is-bold">Building Deeper Connections</h3>	
			<p>Voice control allows our customers to do more than ever before. Similar to smartphone adoption, when features are used frequently, they provide a greater purpose beyond the original technology.</p>
		</div>
		<button class="is-bold show-more-btn">SHOW MORE</button>
		<div class="reveal">
			<p>If customers value staying connected, informed and entertained, and voice control makes it easier to do so, then voice will be a welcomed addition to their lives.<sup>1</sup></p>
			<div class="answer">
				<h3 class="is-bold">Breaking Down Physical Barriers</h3>	
				<p>Customers can interact more freely using their voice than with buttons and menus. Just as the remote control gave way to channel surfing, voice control will also encourage new behaviors and viewing styles that will enhance the user experience.</p>
				<div class="callout">
					<div class="big-quotation"></div>
					<p><span class="is-bold is-purple">The less I need them [caretakers] to do, they could be doing something else. They don’t have to stop doing [their duties] just to change the channel.”</span><br>Richard, Whole Home Accessibility, 10/2015 J. Ackerman</p>
				</div>
				<h3 class="is-bold">Understanding True Intent</h3>	
				<p>While most interfaces predetermine intent with on-screen menus, voice control and the use of natural language allow customers to guide the experience themselves. Even better, by recognizing high-volume voice patterns, we can anticipate needs, improve designs and personalize offerings.</p>
				<div class="callout">
					<div class="big-quotation"></div>
					<p><span class="is-bold is-purple">Can it pull content out of an episode? If I know something I want to see–Seinfeld with the dentist. I’d have to Google or IMDB to find an episode otherwise.”</span><br>Louis, XR-18 Prototype Evaluation and Voice Control Discovery Research, 11/2016, K. Holme</p>
				</div>
