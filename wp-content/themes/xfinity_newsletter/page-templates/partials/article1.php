		<div class="reveal">
			<h3 class="is-bold">As awareness increases, we’re seeing a rise in Voice Assistant use.</h3>
			<div class="footnotes caption">
				<p>Intelligent Voice Assistants User Adoption Survey, MindMeld, February 2016</p>
			</div>
			<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/graph1.png" alt="graph 1" class="graph">

			<div class="divider"></div>

			<div class="answer">
				<h4>Our Top 3 Opportunities:</h4>	
				<h3 class="is-bold large-margin">Building Deeper Connections</h3>	
				<p>Voice control allows our customers to do more than ever before. Similar to smartphone adoption, when features are used frequently, they provide a greater purpose beyond the original technology. If customers value staying connected, informed and entertained, and voice control makes it easier to do so, then voice will be a welcomed addition to their lives.<sup>1</sup></p>	
			</div>

			<h3 class="is-bold large-margin">Breaking Down Physical Barriers</h3>	
			<p>Customers can interact more freely using their voice than with buttons and menus. Just as the remote control gave way to channel surfing, voice control will also encourage new behaviors and viewing styles that will enhance the user experience. Voice technology is also a means to empower people with visual, mobile and manual dexterity impairments, giving them greater independence and freedom from the challenges of a physical remote.</p>
			<div class="callout">
				<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/end-quotation.png" alt="" class="big-quotation">
				<p><span class="is-bold is-purple">The less I need them [caretakers] to do, they could be doing something else. They don't have to stop doing [their duties] just to change the channel.<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/end-quotation.png" alt="" class="end-quotation"></span><span>Richard, Whole Home Accessibility, September 2015, J.&nbsp;Ackerman</span></p>
			</div>
			<h3 class="is-bold large-margin">Understanding True Intent</h3>	
			<p>While most interfaces predetermine intent with onscreen menus, voice control and the use of natural language allow customers to guide the experience themselves. Even better, by recognizing high-volume voice patterns, we can anticipate needs, improve designs and personalize offerings.</p>
			<div class="callout">
				<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/quotation.png" alt="" class="big-quotation">
				<p><span class="is-bold is-purple">Can it pull content out of an episode? If I know something I want to see–<i>Seinfeld</i> with the dentist. I’d have to Google or IMDb to find an episode otherwise.<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/end-quotation.png" alt="" class="end-quotation"></span><span>Louis, XR-18 Prototype Evaluation and Voice Control Discovery Research, November 2016, K.&nbsp;Holmes</span></p>
			</div>
			<div class="divider"></div>
			
			<div class="answer">
				<h4>Our Top 3 Challenges:</h4>	
				<h3 class="is-bold large-margin">Starting the Conversation</h3>	
				<p>Voice control is still an emerging technology and new to many of our customers. The good news? It’s gaining ground quickly. In a 2014 usability study, the concept of a “wake-up word” wasn’t familiar compared to a more recent study in which all participants expected to use a “wake-up word” to get the device to “listen.”<sup>2</sup></p>	
			</div>

			<h3 class="is-bold large-margin">Providing a Comfortable Experience</h3>
			<p>Early adopters want to be sure their technology isn’t disruptive. The stress and frustration of miscommunication were found to be major deterrents to full voice integration. Those who are multilingual and/or have accents are also skeptical.</p>

			<div class="callout">
				<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/quotation.png" alt="" class="big-quotation">
				<p><span class="is-bold is-purple">If I type, I might have misspellings, but my accent leads to misunderstandings too. That annoys me even more than typing.<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/end-quotation.png" alt="" class="end-quotation"></span><span>Boris, XR-18 Prototype Evaluation and Voice Control Discovery Research, November 2016, K.&nbsp;Holmes</span></p>
			</div>

			<h3 class="is-bold large-margin">Showing Long-Term Value</h3>
			<p>Many customers admit that their use of voice control is high at first but drops off over time. Full adoption will take some time simply because customers are more comfortable with the same well-worn paths they’ve always used and found to be effective.</p>
			<div class="callout">
				<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/quotation.png" alt="" class="big-quotation">
				<p><span class="is-bold is-purple">Early-adopter Drew admits to buying his Amazon Echo on a whim when it first went on sale because he thought it 'looked kind of cool.' He now only occasionally uses it to set timers and play music while cooking.<img src="http://xfinity-newsletter/wp-content/themes/xfinity_newsletter/app/images/end-quotation.png" alt="" class="end-quotation"></span><span>Emotional Bond Exploratory Research, February 2016, E.&nbsp;Kell</span></p>
			</div>

			<h3 class="is-bold large-margin">To gain full adoption, customers must fully understand the value proposition.</h3>
